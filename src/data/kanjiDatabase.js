/**
 * 指定した学年の漢字リストを返す
 * @param {number} gradeLevel - 学年（1〜6）
 * @returns {Array} - その学年の漢字オブジェクトの配列
 */
export function getKanjiByGrade(gradeLevel) {
    return KANJI_DATABASE[gradeLevel] || [];
}

/**
 * すべての学年の漢字を一つの配列にまとめて返す
 * @returns {Array} - 全漢字のリスト（学年情報付き）
 */
export function getAllKanji() {
    const allKanji = [];
    Object.entries(KANJI_DATABASE).forEach(([gradeLevel, kanjiList]) => {
        kanjiList.forEach((kanjiItem) => {
            allKanji.push({ ...kanjiItem, grade: parseInt(gradeLevel) });
        });
    });
    return allKanji;
}

/**
 * 一文字の漢字を検索してデータを返す
 * @param {string} kanjiCharacter - 検索する漢字の文字
 * @returns {Object|null} - 見つかった漢字データ、または null
 */
export function findKanji(kanjiCharacter) {
    for (const [gradeLevel, kanjiList] of Object.entries(KANJI_DATABASE)) {
        const found = kanjiList.find((k) => k.kanji === kanjiCharacter);
        if (found) return { ...found, grade: parseInt(gradeLevel) };
    }
    return null;
}
